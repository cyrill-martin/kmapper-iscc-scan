"""
iscc_scan.py — ISCC content inventory tool.

Workspace-based workflow:
  1. Scan directories individually into a shared workspace
  2. Compile a single CSV with clustering from all scanned data

Commands:
    python3 iscc_scan.py scan    /path/to/content   /path/to/workspace
    python3 iscc_scan.py scan    /other/content     /path/to/workspace
    python3 iscc_scan.py scan    /other/content     /path/to/workspace --batch myBatch
    python3 iscc_scan.py compile /path/to/workspace
    python3 iscc_scan.py compile /path/to/workspace --threshold 15
    python3 iscc_scan.py compile /path/to/workspace --similarity 90
    python3 iscc_scan.py status  /path/to/workspace

Workspace structure:
    workspace/
      scan_log.json              ← tracks scanned directories
      sidecars/
        my-project/              ← batch per scanned directory
          subdir/
            file.pdf.iscc.json
        other-content/
          report.docx.iscc.json
      iscc_inventory.csv         ← generated by 'compile'
"""

import csv
import json
import re
import sys
import time
from collections import defaultdict
from datetime import datetime, timezone
from pathlib import Path

try:
    import iscc_sdk as idk
except ImportError:
    sys.exit("iscc-sdk not installed. Run: pip install iscc-sdk")

try:
    import iscc_core as ic
except ImportError:
    sys.exit("iscc-core not installed. Run: pip install iscc-core")


SKIP_PREFIXES = (".", "~")
DEFAULT_THRESHOLD = 10
CONTENT_UNIT_BITS = 64 # Without the 8 bit header!
SIDECARS_DIR = "sidecars"
SCAN_LOG = "scan_log.json"


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def slugify(text: str) -> str:
    """Turn a path into a safe directory name."""
    text = text.strip("/").replace("\\", "/")
    # Use last two path components for readability
    parts = text.split("/")
    slug = "_".join(parts[-2:]) if len(parts) >= 2 else parts[-1] if parts else "scan"
    slug = re.sub(r"[^\w\-.]", "_", slug)
    slug = re.sub(r"_+", "_", slug).strip("_")
    return slug or "scan"


def load_scan_log(workspace: Path) -> dict:
    log_path = workspace/SCAN_LOG
    if log_path.exists():
        with open(log_path) as f:
            return json.load(f)
    return {"scans": []}


def save_scan_log(workspace: Path, log: dict) -> None:
    log_path = workspace/SCAN_LOG
    with open(log_path, "w", encoding="utf-8") as f:
        json.dump(log, f, indent=2, ensure_ascii=False)


def decompose_units(iscc_code: str) -> dict:
    units = {"meta": "", "content": "", "data": "", "instance": ""}
    try:
        parts = ic.iscc_decompose(iscc_code)
        for part in parts:
            explanation = ic.iscc_explain(part)
            maintype = explanation.split("-")[0].lower()
            if maintype in units:
                units[maintype] = part
    except Exception:
        pass
    return units


def hamming_distance(iscc_a: str, iscc_b: str) -> int | None:
    try:
        return ic.iscc_distance(iscc_a, iscc_b)
    except Exception:
        return None


# ---------------------------------------------------------------------------
# Clustering
# ---------------------------------------------------------------------------

def group_by_exact(rows: list[dict], unit_key: str, group_col: str) -> None:
    buckets = defaultdict(list)
    for i, row in enumerate(rows):
        val = row.get(unit_key, "")
        if val:
            buckets[val].append(i)

    group_id = 0
    for indices in buckets.values():
        if len(indices) < 2:
            continue
        group_id += 1
        for i in indices:
            rows[i][group_col] = group_id


def cluster_by_similarity(rows: list[dict], unit_key: str, cluster_col: str,
                          threshold: int) -> int:
    active = [(i, rows[i][unit_key]) for i in range(len(rows)) if rows[i].get(unit_key)]
    parent = list(range(len(active)))

    def find(x):
        while parent[x] != x:
            parent[x] = parent[parent[x]]
            x = parent[x]
        return x

    def union(a, b):
        ra, rb = find(a), find(b)
        if ra != rb:
            parent[ra] = rb

    comparisons = 0
    for ai in range(len(active)):
        for bi in range(ai + 1, len(active)):
            dist = hamming_distance(active[ai][1], active[bi][1])
            comparisons += 1
            if dist is not None and dist <= threshold:
                union(ai, bi)

    clusters = defaultdict(list)
    for ai in range(len(active)):
        clusters[find(ai)].append(active[ai][0])

    cluster_id = 0
    for indices in clusters.values():
        if len(indices) < 2:
            continue
        cluster_id += 1
        for row_idx in indices:
            rows[row_idx][cluster_col] = cluster_id

    return comparisons


